#!/usr/bin/env python

def square_area(side):
   sq = side * side
   return sq

def rectangle_perimeter(sidex, sidey):
   rec = sidex * 2 + sidey * 2
   return rec

def square_perimeter(side):
   sq = side * 4
   return sq

def circle_area(radius):
   circ = 3.14 * radius ** 2
   return circ

def circle_circumference(radius):
   circ = 2 * 3.14 * radius
   return circ
